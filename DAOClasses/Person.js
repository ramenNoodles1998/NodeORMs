class Person {
    constructor (id, first, middle, last, dob, phone, email, street, city, state, zip) {
        this.id = id;
        this.first = first;
        this.middle = middle;
        this.last = last;
        this.dob = dob;
        this.phone = phone;
        this.email = email;
        this.street = street;
        this.city = city;
        this.state = state;
        this.zip = zip;
    }
}

module.exports = Person